    private void isiJlhKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_isiJlhKeyReleased
        // TODO add your handling code here:
        String hrg = null ;
        String stok = null ;
        try {
            String sql = "select * from data_obat where Nama like '" + isiNamaObat.getSelectedItem() + "'";
            java.sql.Connection MySQLConfig = (Connection)Konfig.configDB();
            java.sql.Statement stm = MySQLConfig.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                stok = (res.getString("Stok"));
                hrg = (res.getString("Harga"));
            }
            
            } catch (SQLException e) {
            System.out.println("Error, harga tidak boleh kosong " + e.getMessage());
        }
        
        int stokbrg = Integer.parseInt(stok);
        int hrgbeli = Integer.parseInt(hrg);
        int jmlbeli = Integer.parseInt(isiJlh.getText());
        int a = Integer.parseInt(stok);
        
        if(a < jmlbeli){
            JOptionPane.showMessageDialog(null,"Maaf Stok Tidak Cukup!!");
            KosongForm();
        }else{
            jmlbeli=(hrgbeli*jmlbeli);
            isiTotbay.setText(String.valueOf(jmlbeli));
            isiHrg.setText(String.valueOf(hrgbeli));
        }
    }//GEN-LAST:event_isiJlhKeyReleased

